# Leia quatro notas, calcule a média aritmética e imprima o resultado
notaA = float(input('Digite a nota A: '))
notaB = float(input('Digite a nota B: '))
notaC = float(input('Digite a nota C: '))
notaD = float(input('Digite a nota D: '))
media = (notaA + notaB + notaC + notaD) / 4
print(f'A média das devidas notas é igual a {media:.1f}')
