# Faça a leitura de três valores e apresente como resultado a soma do quadrado dos três valores
valorA = float(input('Digite o valor A: '))
valorB = float(input('Digite o valor B: '))
valorC = float(input('Digite o valor C: '))
print(f'A soma do quadrados dos três valores é igual a {(valorA ** 2) + (valorB ** 2) + (valorC ** 2)}')